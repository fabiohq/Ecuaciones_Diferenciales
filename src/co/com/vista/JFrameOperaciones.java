        labelRespuesta3.setText(tokens[2]);
        labelRespuesta4.setText(tokens[3]);
        labelRespuesta5.setText(tokens[4]);
        labelRespuesta6.setText(tokens[5]);

        datosGrafica.addValue(datos.getPoblacionInicial(), "Poblacion Inicial", "Poblacion Inicial");
        datosGrafica.addValue(datos.getPoblacionFinal(), "Poblacion Final", "Poblacion Final");
        grafica = ChartFactory.createBarChart3D("Aumento poblacional", "Tiempo", "Cantidad Habitantes", datosGrafica, PlotOrientation.VERTICAL, true, true, false);
